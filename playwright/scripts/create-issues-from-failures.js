#!/usr/bin/env node
'use strict';

/**
 * create-issues-from-failures.js
 *
 * Reads Playwright JSON test results and creates GitHub issues for each failure.
 * Attaches failure screenshots when available.
 *
 * Usage:
 *   # After running tests with JSON reporter:
 *   npx playwright test --reporter=json > test-results.json
 *   node scripts/create-issues-from-failures.js
 *
 *   # Or with custom paths:
 *   RESULTS_FILE=./custom-results.json REPO=jperezdelreal/pixel-bounce node scripts/create-issues-from-failures.js
 *
 * Environment variables:
 *   RESULTS_FILE  — Path to Playwright JSON results (default: ./test-results.json)
 *   REPO          — GitHub repo in owner/repo format (auto-detected from git if unset)
 *   LABEL         — Issue label (default: "squad,bug,playwright")
 *   DRY_RUN       — If "true", prints issues but doesn't create them
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const RESULTS_FILE = process.env.RESULTS_FILE || path.resolve(__dirname, '..', 'test-results.json');
const LABEL = process.env.LABEL || 'squad,bug,playwright';
const DRY_RUN = process.env.DRY_RUN === 'true';

function getRepo() {
  if (process.env.REPO) return process.env.REPO;
  try {
    const url = execSync('git remote get-url origin', { encoding: 'utf8' }).trim();
    const match = url.match(/github\.com[:/](.+?)(?:\.git)?$/);
    return match ? match[1] : null;
  } catch {
    return null;
  }
}

function findScreenshot(testResult) {
  // Playwright stores attachments in test results
  if (testResult.attachments) {
    for (const att of testResult.attachments) {
      if (att.name === 'screenshot' && att.path) {
        return att.path;
      }
    }
  }
  return null;
}

function formatIssueBody(suite, testCase, result) {
  const screenshot = findScreenshot(result);
  const errorMsg = result.error?.message || 'No error message captured';
  const errorStack = result.error?.stack || '';
  const duration = result.duration ? `${(result.duration / 1000).toFixed(1)}s` : 'unknown';

  let body = `## 🎮 Playwright Gameplay Test Failure

**Test Suite:** \`${suite}\`
**Test Name:** \`${testCase.title}\`
**Duration:** ${duration}
**Status:** ❌ ${result.status}

### Error
\`\`\`
${errorMsg}
\`\`\`

### Stack Trace
<details>
<summary>Click to expand</summary>

\`\`\`
${errorStack}
\`\`\`
</details>

### Reproduction
\`\`\`bash
npx playwright test --grep "${testCase.title}"
npx playwright test --grep "${testCase.title}" --debug  # Interactive debugging
\`\`\`

### Context
- **Generated by:** \`create-issues-from-failures.js\` (Playwright template)
- **Game:** Pixel Bounce
- **Timestamp:** ${new Date().toISOString()}
`;

  if (screenshot && fs.existsSync(screenshot)) {
    body += `\n### Screenshot\n_Screenshot attached to this issue._\n`;
  }

  body += `\n---\n_Auto-created by Syntax Sorcery Playwright testing framework. Close when fixed._`;

  return body;
}

function createIssue(repo, title, body, screenshotPath) {
  const safeTitle = title.replace(/"/g, '\\"');

  // Ensure labels exist (create if missing, ignore errors)
  const labels = LABEL.split(',').map(l => l.trim());
  for (const label of labels) {
    try {
      execSync(`gh label create "${label}" --repo "${repo}" --color "d73a4a" --force 2>nul`, {
        encoding: 'utf8',
        stdio: 'pipe',
      });
    } catch {
      // Label may already exist — that's fine
    }
  }

  let cmd = `gh issue create --repo "${repo}" --title "${safeTitle}" --body "${body.replace(/"/g, '\\"').replace(/\n/g, '\\n')}" --label "${LABEL}"`;

  if (DRY_RUN) {
    console.log(`\n[DRY RUN] Would create issue:`);
    console.log(`  Repo:  ${repo}`);
    console.log(`  Title: ${title}`);
    console.log(`  Labels: ${LABEL}`);
    if (screenshotPath) console.log(`  Screenshot: ${screenshotPath}`);
    return;
  }

  try {
    const result = execSync(cmd, { encoding: 'utf8', stdio: 'pipe' });
    const issueUrl = result.trim();
    console.log(`✅ Created issue: ${issueUrl}`);

    // Attach screenshot as a comment if available
    if (screenshotPath && fs.existsSync(screenshotPath)) {
      try {
        const issueNumber = issueUrl.match(/\/issues\/(\d+)/)?.[1];
        if (issueNumber) {
          const screenshotComment = `Screenshot from test failure:\n\n![failure-screenshot](${screenshotPath})`;
          execSync(
            `gh issue comment ${issueNumber} --repo "${repo}" --body "Screenshot attached. See test-results/ directory for full artifacts."`,
            { encoding: 'utf8', stdio: 'pipe' }
          );
          console.log(`  📸 Screenshot note added to issue #${issueNumber}`);
        }
      } catch (e) {
        console.warn(`  ⚠️  Could not attach screenshot: ${e.message}`);
      }
    }

    return issueUrl;
  } catch (e) {
    console.error(`❌ Failed to create issue: ${e.message}`);
    return null;
  }
}

function main() {
  console.log('🎭 Playwright → GitHub Issues\n');

  const repo = getRepo();
  if (!repo) {
    console.error('❌ Could not determine repo. Set REPO=owner/repo env var.');
    process.exit(1);
  }
  console.log(`📦 Repo: ${repo}`);
  console.log(`🏷️  Labels: ${LABEL}`);
  if (DRY_RUN) console.log('🔍 DRY RUN MODE — no issues will be created\n');

  if (!fs.existsSync(RESULTS_FILE)) {
    console.error(`❌ Results file not found: ${RESULTS_FILE}`);
    console.error('   Run tests first: npx playwright test --reporter=json > test-results.json');
    process.exit(1);
  }

  const raw = fs.readFileSync(RESULTS_FILE, 'utf8');
  let results;
  try {
    results = JSON.parse(raw);
  } catch {
    console.error('❌ Could not parse test results JSON');
    process.exit(1);
  }

  const failures = [];

  // Walk the Playwright JSON result structure
  for (const suite of (results.suites || [])) {
    const suiteName = suite.title || 'Unknown Suite';
    for (const spec of (suite.specs || [])) {
      for (const test of (spec.tests || [])) {
        for (const result of (test.results || [])) {
          if (result.status === 'failed' || result.status === 'timedOut') {
            failures.push({ suiteName, spec, test, result });
          }
        }
      }
    }
    // Handle nested suites (Playwright groups tests in describe blocks)
    for (const innerSuite of (suite.suites || [])) {
      const innerSuiteName = `${suiteName} > ${innerSuite.title || 'Unknown'}`;
      for (const spec of (innerSuite.specs || [])) {
        for (const test of (spec.tests || [])) {
          for (const result of (test.results || [])) {
            if (result.status === 'failed' || result.status === 'timedOut') {
              failures.push({ suiteName: innerSuiteName, spec, test, result });
            }
          }
        }
      }
    }
  }

  if (failures.length === 0) {
    console.log('\n✅ All tests passed — no issues to create.');
    process.exit(0);
  }

  console.log(`\n❌ Found ${failures.length} failure(s):\n`);

  let created = 0;
  for (const { suiteName, spec, result } of failures) {
    const title = `[Playwright] ${spec.title} — ${suiteName}`;
    const body = formatIssueBody(suiteName, spec, result);
    const screenshot = findScreenshot(result);

    console.log(`  → ${title}`);
    const url = createIssue(repo, title, body, screenshot);
    if (url) created++;
  }

  console.log(`\n📊 Summary: ${created}/${failures.length} issues created.`);
}

main();
